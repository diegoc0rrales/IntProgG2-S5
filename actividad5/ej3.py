dia=int(input("Escribe el dia de la semana en forma de numero(1-7): "))


if dia==1:
    print("Lunes") 
elif dia ==2:
    print("Martes")
elif dia ==3:
    print("Miércoles")
elif dia ==4:
    print("Jueves")
elif dia ==5:
    print("Viernes")
elif dia ==6:
    print("Sábado")
elif dia ==7:
    print("Domingo")
else:
    print("Error. Número no válido")
